    private void ButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSaveActionPerformed
        String ProductId = TxtProductId.getText();
        String ProductName = TxtProductName.getText();
        String Stock = TxtStock.getText();        
        if (ProductId.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Product id tidak boleh kosong");
            TxtProductId.requestFocus();
        }else if (ProductName.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Nama product tidak boleh kosong");
            TxtProductName.requestFocus();
        }else if (Stock.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Stock tidak boleh kosong");
            TxtStock.requestFocus();
        }else{
            try{
                Connection conn=(Connection)Koneksi.getConnection();
                Statement stt=conn.createStatement();
                stt.executeUpdate("insert into Product(Product_Name,Stock)"+
                    "VALUES('"+ProductName+"','"+Stock+"')");
                ClearData();
                ShowData();
                SetEditOff();
                JOptionPane.showMessageDialog(this,"Data berhasil disimpan","Success",
                        JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Simpan data gagal\n"+e.getMessage(),"Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_ButtonSaveActionPerformed

