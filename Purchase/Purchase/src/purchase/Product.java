    public void ShowData() {
        DftTblModel_barang = new DefaultTableModel();
        DftTblModel_barang.addColumn("Product Id");
        DftTblModel_barang.addColumn("Product Name");
        DftTblModel_barang.addColumn("Stock");
        TableProduct.setModel(DftTblModel_barang);
        Connection conn = Koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from Product";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_barang.addRow(new Object[]{
                    res.getString("Product_ID"),
                    res.getString("Product_Name"),
                    res.getString("Stock"),
                });}
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
